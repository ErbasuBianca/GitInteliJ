public class Variabila {

    public static void main(String[] args) {

        // aici vom scrie codul
        // int accepta numere intregi
        int numar = 10;

        //double este folosit pe numere cu zecimale
        double numarCuZecimale = 10.5;

        // String este folosit pentru text
        String text = "salut";

        //boulean care stocheaza true or false
        boolean decizie = false;

        System.out.println("Numarul nostru este: " +numar);
        System.out.println("Numarul nostru este: "+numarCuZecimale);
        System.out.println("Textul nostru este: "+text);
        System.out.println("Variabile noastra decizional este : "+decizie);
    }
}
